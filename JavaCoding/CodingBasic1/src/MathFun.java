import java.util.Scanner;

public class MathFun {
    public static void main(String[] args) {

        System.out.println("max : " + Math.max(100,50));
        System.out.println("min : " + Math.min(100,50));
        System.out.println("abs : " + Math.abs(-12));
        System.out.println("sqrt : " + Math.sqrt(4));
        System.out.println("random : " + Math.random());  // 0 to 1 between number
        System.out.println("degrees : " + Math.toDegrees(3600));
        System.out.println("radians : " + Math.toRadians(3600));
        System.out.println("log : " + Math.log(10));
        System.out.println("log10 : " + Math.log10(10));


    }
}
